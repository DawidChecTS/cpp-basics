#include <iostream>

                        // Fundamental types

int a = 1;              // type of int stores 4 bytes of memory -2B to 2B
short b = 2;            // type of short stores 2 bytes of memory -32,768 to 32,767
long c = 3;             // type of long stores 4 bytes of memory -2B to 2B  
long long d = 4;        // type of long long stores 8 bytes
 
                        // Floating point numbers

float e = 5.0;          // type of float stores 4 bytes of memory -3.4E308 to 3.4E308
double f = 6.0;         // type of double stores 8 bytes of memory -1.7E308 to 1.7E308
long double g = 7.0;    // type of long double stores 8 bytes of memory -3.4E932 to 3.4E932

                        // Other types

bool dawid;             // is true or false
char awsedasd;          // for storing characters